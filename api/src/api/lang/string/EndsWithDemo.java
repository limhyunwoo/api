package api.lang.string;

/*
 * java.lang.endwith
 * endswith(s) : s 로 끝나는지 여부체크 
 */

public class EndsWithDemo {
	public static void main(String[] args) {
		System.out.println(
				"Hello.txt".endsWith("txt") ? "txt 파일입니다" : "txt 파일이 아닙니다"); // hello.txt 를 변수취급해서 사용할 수 있음.
	}
	
	
	
}
